                <h5>Dress Code</h5>
                <br/>
                <p>As at every UN conference, in order to keep the integrity of the event, all delegates at UFRGSMUN are required to observe a western business attire, or national equivalent. For example, men should wear full suit (blazer and dress pants), dress shirt, tie and dress shoes. Similarly, women should wear a blouse and dress pants or skirt, blazer or sweater. If the representation of your delegation can be enhanced by wearing a formal national dress, you may do so as long as it is carried out with attention to detail and convention, and with the maximum of respect.</p>

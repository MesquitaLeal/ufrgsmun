                <h5>General Policies</h5>

                <br/>

                <p>Every UFRGSMUN participant ought to read these policies in their entirety and abide to them during the conference. Delegates are also expected to abide by all local laws and venue-specific regulations while at UFRGSMUN 2014.</p>

                <br/>

                <p><strong>Delegations</strong></p>
                <p>Delegations must be composed from 1 (one) up to 20 (twenty) delegates. The number of representatives in a delegation must be equivalent to the number of spots requested upon registration.</p>

                <br/>

                <p><strong>Head Delegates</strong></p>
                <p>Each delegation is required to have a head delegate, who is a student on the delegation (this does not apply to single or individual delegates, who act as head delegates automatically). The head delegate will serve as the delegation ‘captain’ and will be the primary liaison for UFRGSMUN updates and administrative emails from the Secretariat. Head delegates will also attend Head Delegate & Faculty Advisor Meetings during the conference where the Secretary-General will provide updates.</p>

                <br/>

                <p><strong>Country Assignment</strong></p>
                <p>Country assignments will take place after the closure of the first step for registration (Form A), between October 1st and October 5th. The registration fee will not be refunded even if a delegation does not accept its country assignment. Delegations, especially the Head Delegate, should pay close attention to the Country/Committee Matrix which lists which countries and actors are represented in each committee. Delegates from different universities representing the same country on different committees do not have to confer when representing national policies, but are advised to do so.</p>
                <p>The UFRGSMUN Secretariat reserves the right to remove spots from an already assigned country in the event that the delegation fails to fill all the assigned spots. Delegations are required to assign at least one delegate to all designated committees (i.e.: if a delegate cannot attend conference, another delegate from the same school in a double-delegate committee will be required to take up the open spot).</p>
                <p>The Secretariat also reserves the right to assign delegations to countries which were not previously listed on the Country/Committee Matrix in extraordinary cases.</p>
                <p>NOTE: Submitting a country preference is not a guarantee to receiving this country assignment, and all decisions of the UFRGSMUN Secretariat with regard to country assignments are final.</p>

                <br/>

                <p><strong>Delegate Fee</strong></p>
                <p>Head delegates will have the chance to choose in the registration form whether their delegations will pay their corresponding delegate fees in two installments or in full amount. The payment method chosen necessarily applies to all members of your delegation.</p>
                <p>Head delegates are responsible for gathering the money of their delegations and sending the payment to UFRGSMUN.</p>

                <br/>

                <p><strong>Special Circumstances</strong></p>
                <p>If meeting the requirements for payment is impossible for any reason, please contact UFRGSMUN staff as soon as it becomes evident that the requirements will not be fulfilled. The Secretary-General and the Under-Secretary-General for Administrative Affairs are more than happy to discuss individual circumstances with a delegation.</p>

                <br/>

                <p><strong>Refunds</strong></p>
                <p>Registration fees are non-refundable.</p>

                <br/>

                <p><strong>Suspension Policy</strong></p>
                <p>In order to maintain order at the conference as well as our relationship with our venue and social events hosts, our policy requires that any delegate violating the following rules will be subject to an immediate suspension from UFRGSMUN 2014. Observing them is the collective responsibility of the entire delegation, which will not be eligible for delegation awards, if one of its delegates is found violating these rules.</p>

                <br/>

                <p><strong>Alcohol and Drug Policy</strong></p>
                <p>Only those delegates age 18 or older may consume alcohol in Brazil. Open containers of alcohol may not be carried in the conference venue. Furthermore, delegates found possessing or using illicit drugs, as defined by local federal law, besides being immediately suspended from UFRGSMUN 2014, may be held criminally liable.</p>

                <br/>

                <p><strong>Smoking Policy</strong></p>
                <p>Porto Alegre’s local regulations prohibit smoking indoors in public spaces. Delegates will not be permitted to smoke inside the conference centre or at social event venues.</p>

                <br/>

                <p><strong>Valuables</strong></p>
                <p>UFRGSMUN 2014 conference and social events venues are not responsible for belongings left in committee rooms, social events venues or buses. However, if there is any missing object, the UFRGSMUN staff will help in any way possible. Moreover, if a delegate needs assistance in keeping an object safe, he/she should contact the administrative staff, which will be happy to help.</p>

                <br/>

                <p><strong>Committee Session Policy</strong></p>
                <p>Delegates are expected to be in their respective committee rooms during committee sessions. In order for delegates to leave their committee rooms for an extended period of time, both their Chair and their Head Delegate/Faculty Advisor must be informed ahead of time. Otherwise, they will be considered missing the session. Furthermore, all delegates are expected to abide by UFRGSMUN’s dress code.</p>

                <br/>

                <p><strong>Certificate of Participation</strong></p>
                <p>The certificates of participation will be handed over to the delegates at the last session on the last day of UFRGSMUN 2014 (November 4th). Delegates have to be present in at least 75% of all committee sessions in order to receive it.</p>

                <br/>

                <p><strong>Transportation</strong></p>
                <p>UFRGSMUN’s official transportation has a tight schedule. Delegates are therefore expected to be punctual in order to make use of it. The drivers are not going to wait for late delegates and neither turning back to pick up someone who was late. In such cases, the delegates will have to go to the venues at their own expenses.</p>

                <br/>

                <p><strong>Official Accommodation</strong></p>
                <p>Participants are responsible for their own accommodation arrangements. UFRGSMUN is not liable for any room booking or commitment made by participants with any hotels and hostels in Porto Alegre.</p>
                <p>Accommodations at the official hotels are limited and will be filled on a first-come, first-served basis. If participants do not manage to get a spot at our official hotels and/or hostels, it is their sole responsibility to look for another accommodation option on their own. Costs may be considerably higher. UFRGSMUN is neither liable for the additional costs incurred nor will it refund registration fees in case of conference withdrawal</p>

                <br/>

                <p><strong>Other Rules</strong></p>
                <p>The Secretariat reserves the right to immediately expel delegates from both the conference and the conference centre due to unacceptable behavior. Serious infractions on the part of even a single delegate may require the suspension without refund of the individual involved and his or her delegation from the conference. The Secretariat reserves the right to define appropriate conference behavior and is particularly sensitive to issues involving displays of disrespect and violence to other delegates, UFRGSMUN staff, conference centre staff and social events venues’ staff.</p>

                <br/>

                <p><strong>Exceptions</strong></p>
                <p>The Secretary-General, João Arthur da Silva Reis, and the Under-Secretary-General for Administrative Affairs, Bruna Leão Lopes Contieri and Júlia Oliveira Rosa, are the only staff members empowered to grant exceptions to any conference policies or other general UFRGSMUN practices. Delegations will not be allowed to register at the conference until they have paid any outstanding debts to UFRGSMUN. If a delegate or delegation withdraws from the conference, the school is liable for all fees charged up to the date of withdrawal. Requests for exceptions must be submitted in writing to <a href="mailto:ufrgsmun@ufrgs.br">ufrgsmun@ufrgs.br</a>.</p>
                <p>NOTE: UFRGSMUN is not liable for any erroneous statements made by other staff members regarding exceptions to fees or policies.</p>


        <h6>Maritime connectivity in the Asia-Pacific Region</h6>
        <p>
            The Asia-Pacific Region generally includes countries from East Asia, Southeast Asia and
            Oceania. Most of them rely on water to create jobs, to enable trade relations and, therefore,
            to prosper economically. Bearing this in mind, the maritime infrastructure is a key element
            to these countries’ economic growth and to reduce poverty of the region, which is the main
            objective of the Asian Development Bank (ADB, 2006, p. iv). Besides improving the conditions
            of the countries, reliable shipping services and an adequate infrastructure of ports, canals
            and waterways can improve economic integration of the economies from the Asia-Pacific Area,
            easing trade connections and transport links among them.
        </p>
        <p>
            In the archipelagos of the Southeast Asia, trade is mostly done by sea (ADB, 2009, p.3). One
            of the biggest obstacles of this region is the matter of infrastructure, due to its archipelagic
            geography, which requires integrated planning and appropriate financing in order overcome
            adversities of poor infrastructure connections. “The lack of infrastructure poses serious
            barriers to development in the poorer, border islands of Indonesia and the Philippines, and
            in Sarawak and Sabah in Malaysia.” (ADB, 2006, p.24). In Northeast Asia, a subregion of East
            Asia, there are also infrastructure bottlenecks to be addressed.
        </p>
        <p>
            In order to fully develop the area, and also to benefit from existing economic cooperation
            opportunities, “it is essential that an appropriate transport and logistics system including
            adequate port and shipping services be developed to facilitate the efficient and safe flow
            of trade within the region as well as to and from overseas markets” (UNESCAP, 2005, p. 1).
            In Japan, South Korea and China, maritime shipping and ports have already played a key role
            in improving their trade ties, but further developments can still be made, especially concerning
            a better integration of the area and the Democratic People’s Republic of Korea issues. Moreover,
            Russian proximity comes up with new opportunities to build pipelines and further energy
            infrastructures.
        </p>
        <p>
            Regarding the subject brought here, the ADB can contribute with appropriate financing,
            advising and development of projects that involve the maritime connections of the regions
            above quoted. The ADB has the clear objective of reducing poverty and of promoting regional
            cooperation and integration, owning the means to commit its resources to such matters.
            Improving maritime connectivity is essential to both of these goals, especially considering
            the unique geographical conditions of the Asia-Pacific Region.
        </p>

        <h5>International Press Committee</h5>
            <p>
                In 2014, for the first time, UFRGSMUN will have delegates simulating a press agency.
                It will work as an exercise of collecting and sharing information about what is being
                discussed in the committees. The International Press Committee was created with the
                purpose of having delegates simulating real communication agencies, such as BBC, Al
                Jazeera, among others. This will happen inside the committees, producing content which
                follows the editorial line of such media vehicles. The press delegates will simulate
                either one or two agencies which are related to an specific committee, depending on
                the scope of the topic and/or geography. They will work exclusively as reporters to
                these vehicles, writing articles which agree with the editorial lines.
            </p>
